let fruits = ['apple','orange',5,'mango',9.45,null]

// push

fruits.push('pineapple')
console.log(fruits)

// pop

let popped = fruits.pop()
console.log(fruits)
console.log(`popped value is : ${popped}`)


//unshift

fruits.unshift('banana')
console.log(fruits);

//shift

let shifted = fruits.shift()
console.log(fruits)
console.log(`shifted fruit is ${shifted}`)